import mongoose from "mongoose";
const Schema = new mongoose.Schema({
    username:{
        type:String,
        required:true,
        unique:true
    },
    password:{
        type:String,
        required:true
    },
    email:{
        type:String,
        required:true,
        unique:true
    },
    phone:{
        type:String,
        required:true,
        unique:true

    }

    
});
export default mongoose.model.Logins || mongoose.model("Login",Schema);